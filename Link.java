
class Link
{
    public Location dData;                // data item
    public Link next;                 // next link in list
    // -------------------------------------------------------------
    public Link(Location d)               // constructor
    { dData = d; }
    // -------------------------------------------------------------
    public void displayLink()         // display this link
    { System.out.print(dData + " "); }
    // -------------------------------------------------------------
}  // end class Link
